using System;

namespace OperatorsMosh
{
    class Program
    {
        static void Main(string[] args)
        {
            //Operators
            int a = 1;
            int b = a++;
            Console.WriteLine("a = {0} ",a);
            Console.WriteLine("b = {0} ", b);

            int c = 1;
            int d = ++c;
            Console.WriteLine("c = {0}", c);
            Console.WriteLine("d = {0}", d);





        }
    }
}
